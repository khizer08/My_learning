let size=prompt("enter the size: ");
if(size=="XL"){
    console.log("The price is 250");
}
else if(size=="L"){
    console.log("The price is 200");
}
else if(size=="M"){
    console.log("The price is 100");
}
else if(size=="S"){
    console.log("The price is 50");
}
else{
    console.log("Enter the valid size");
}